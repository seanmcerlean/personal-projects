def position_at(position, direction):
    move = {"S": (1, 0), "N": (-1, 0), "W": (0, -1), "E": (0, 1)}
    
    return (position[0] + move[direction][0], position[1] + move[direction][1])
    
def find_path(position):
    global map
    
    if position == (10, 10):
        return True
        
    if map[position[0]][position[1]]:
        return False
    
    if map[position[0]][position[1]] == '+':
        return False
        
    map[position[0]][position[1]] = '+'
    
    result.append('N')
    if (find_path(position_at(position, 'N')) == True):
        return True
    result.pop()
    
    result.append('E')
    if (find_path(position_at(position, 'E')) == True):
        return True
    result.pop()
    
    result.append('S')
    if (find_path(position_at(position, 'S')) == True):
        return True
    result.pop()
    
    result.append('W')
    if (find_path(position_at(position, 'W')) == True):
        return True
    result.pop()
    
    map[position[0]][position[1]] = 0
    
    return False

def checkio(maze_map):
    global map, result
    
    result = []
    
    map = maze_map
    
    find_path(start)
    
    return ''.join(result)


